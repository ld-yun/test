/*****************************************
打印??000整数范围内，满足个位数的立方等于
其本身的所有整数（用printf 函数输出）?
*****************************************/

#include<stdio.h>
int main(void)
{
    int i,a;
    for(i=1; i<1000; i++)
    {
       a = i % 10;
       if(i == a*a*a)
       {
           printf("%d ",i);
       }
    }
    return 0;
}